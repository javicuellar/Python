#   Función para encontrar el número mayor (máximo) de la lista dada
def find_longest(arr):
	max = 0
	for i in arr:
		if i > max:
			max = i
	return max

#   Función idéntica a la anterior con lambda
def maximo(arr):
	return max(arr, key=lambda x: len(str(x)))


